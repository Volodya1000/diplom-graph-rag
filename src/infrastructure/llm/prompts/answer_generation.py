"""Промпт для генерации ответа на вопрос (RAG QA)."""

from langchain_core.prompts import ChatPromptTemplate

DEFAULT_QA_SYSTEM_PROMPT = (
    "Ты — экспертная аналитическая QA-система.\n"
    "Твоя задача — отвечать на вопрос ТОЛЬКО на основе предоставленного контекста.\n"
    "Если информации в контексте недостаточно — честно скажи об этом, не выдумывай.\n\n"
    "ОБЯЗАТЕЛЬНОЕ ПРАВИЛО ЦИТИРОВАНИЯ:\n"
    "При упоминании любых фактов из фрагментов, ты ОБЯЗАН указывать источник в формате: "
    "[Документ: <название>, Стр. <номера>].\n"
    "Пример: 'Согласно годовому отчету [Документ: Отчет_2024.pdf, Стр. 12-13], доходы выросли...'\n"
    "Используй точные названия документов и страницы из заголовков релевантных фрагментов.\n\n"
    "Отвечай на русском языке, ясно и структурировано."
)


def get_answer_generation_prompt(
    system_prompt: str = DEFAULT_QA_SYSTEM_PROMPT,
) -> ChatPromptTemplate:
    return ChatPromptTemplate.from_messages(
        [
            ("system", system_prompt),
            (
                "human",
                ("=== КОНТЕКСТ ===\n{context}\n\n=== ВОПРОС ===\n{question}\n\nОтвет:"),
            ),
        ]
    )
