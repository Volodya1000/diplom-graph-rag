from langchain_core.prompts import ChatPromptTemplate

DEFAULT_QA_SYSTEM_PROMPT = (
    "Ты — экспертная аналитическая QA-система.\n"
    "Отвечай кратко: максимум 2 абзаца. Каждый абзац — 3–4 предложения.\n"
    "Отвечай ТОЛЬКО на основе предоставленного контекста.\n"
    "ВАЖНО: В исходном тексте контекста могут быть опечатки или ошибки распознавания (OCR) "
    "Обязательно исправляй их, формулируя ответ грамотным языком "
    "с использованием правильных и нормализованных терминов.\n\n"
    "ОБЯЗАТЕЛЬНОЕ ПРАВИЛО ЦИТИРОВАНИЯ:\n"
    "При упоминании любых фактов указывай источник в формате:\n"
    '[Документ: <название>, Стр. <номера>, Заголовки: "<полный путь через →>"]\n\n'
    "Пример:\n"
    "Согласно политике[Документ: politica_voznagrazhdeniya.pdf, Стр. 3, "
    'Заголовки: "Политика вознаграждения → Система вознаграждений членов Правления"] '
    "вознаграждения привязаны к результатам и рискам.\n\n"
    "Если заголовков несколько — перечисляй их все через «→». "
    "Если заголовков нет — можно опустить часть «Заголовки: ...».\n"
    "Отвечай на русском языке, ясно и структурировано."
)


def get_answer_generation_prompt(
    system_prompt: str = DEFAULT_QA_SYSTEM_PROMPT,
) -> ChatPromptTemplate:
    return ChatPromptTemplate.from_messages(
        [
            ("system", system_prompt),
            (
                "human",
                ("=== КОНТЕКСТ ===\n{context}\n\n=== ВОПРОС ===\n{question}\n\nОтвет:"),
            ),
        ],
    )
