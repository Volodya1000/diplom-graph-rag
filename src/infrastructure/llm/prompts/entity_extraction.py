from langchain_core.prompts import ChatPromptTemplate


def get_entity_extraction_prompt() -> ChatPromptTemplate:
    system_message = (
        "Ты — аналитик текста и эксперт по графам знаний. "
        "Твоя цель — извлекать именованные сущности и отношения между ними.\n"
        "Отвечай ТОЛЬКО валидным JSON. "
        "Никаких приветствий, комментариев, тегов <think>.\n\n"
        "{format_instructions}"
    )

    human_message = (
        "=== ОНТОЛОГИЯ (ТИПЫ СУЩНОСТЕЙ) ===\n"
        "{tbox_classes}\n\n"
        "=== ДОПУСТИМЫЕ ОТНОШЕНИЯ ===\n"
        "{tbox_relations}\n\n"
        "=== УЖЕ ИЗВЕСТНЫЕ СУЩНОСТИ ===\n"
        "{known_entities}\n\n"
        "=== ПРАВИЛА ИЗВЛЕЧЕНИЯ СУЩНОСТЕЙ ===\n"
        "1. ЯЗЫК (КРИТИЧНО): Всегда переводи названия сущностей на РУССКИЙ язык (если это не имя собственное бренда или ИТ-продукта). "
        "Никогда не смешивай языки. Например, вместо 'CyberSecurityDepartment' пиши 'Департамент кибербезопасности', вместо 'RiskCulture' — 'Риск-культура'.\n"
        "2. РАЗМЕР И ФОРМАТ: Сущность должна быть коротким термином (1-4 слова). ЗАПРЕЩЕНО извлекать целые предложения или абзацы как сущность.\n"
        "3. ОЧИСТКА OCR: Текст часто содержит ошибки распознавания (например, 'Блнк' вместо 'Банк'). Исправляй опечатки.\n"
        "4. ЗАПРЕЩЕНО извлекать: заголовки, номера статей, названия разделов ('ГЛАВА 14', 'Статья 49', 'СОДЕРЖАНИЕ').\n"
        "5. Если сущность из текста совпадает с уже известной, используй ТОЧНО ТО ЖЕ имя и тип.\n"
        "=== ПРАВИЛА ДЛЯ ОТНОШЕНИЙ ===\n"
        "6. Извлекай как можно БОЛЬШЕ значимых отношений (троек).\n"
        "7. Используй предикаты СТРОГО из списка допустимых.\n"
        "8. 'subject' и 'object' ДОЛЖНЫ точно совпадать с 'name' извлечённых сущностей.\n"
        "9. Без дубликатов. Без самоссылок.\n\n"
        "=== ТЕКСТ ===\n"
        "{text}"
    )

    return ChatPromptTemplate.from_messages(
        [
            ("system", system_message),
            ("human", human_message),
        ],
    )
