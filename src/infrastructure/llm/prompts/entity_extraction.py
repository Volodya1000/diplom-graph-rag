"""
Промпт для извлечения сущностей И троек (subject–predicate–object).
"""

from langchain_core.prompts import ChatPromptTemplate


def get_entity_extraction_prompt() -> ChatPromptTemplate:
    system_message = (
        "Ты — анализатор текста. "
        "Твоя задача — извлекать именованные сущности "
        "и отношения между ними.\n"
        "Ты ДОЛЖЕН отвечать строго в формате JSON.\n"
        "Никаких приветствий, размышлений, тегов <think>.\n\n"
        "{format_instructions}"
    )

    human_message = (
        "=== ОНТОЛОГИЯ ===\n\n"
        "Типы сущностей (иерархия классов):\n"
        "{tbox_classes}\n\n"
        "Допустимые отношения между типами:\n"
        "{tbox_relations}\n\n"
        "=== ПРАВИЛА ===\n"
        "1. Извлеки ВСЕ именованные сущности из текста.\n"
        "2. Для каждой сущности укажи type из онтологии. "
        "   Если подходит подкласс — используй его, а не родителя.\n"
        "   Если ничего не подходит — предложи свой тип "
        "   (на английском, CamelCase).\n"
        "3. Извлеки ВСЕ отношения между найденными сущностями "
        "   как тройки (subject, predicate, object).\n"
        "4. predicate — в UPPER_SNAKE_CASE. "
        "   Предпочитай предикаты из списка допустимых отношений. "
        "   Если нужного нет — предложи свой.\n"
        "5. subject и object в тройках ДОЛЖНЫ точно совпадать "
        "   с полем name одной из извлечённых сущностей.\n"
        "6. Отношение допустимое для родительского типа "
        "   допустимо и для его подклассов.\n"
        "7. Возвращай ТОЛЬКО валидный JSON.\n\n"
        "=== ТЕКСТ ===\n"
        "{text}"
    )

    return ChatPromptTemplate.from_messages([
        ("system", system_message),
        ("human", human_message),
    ])