from langchain_core.prompts import ChatPromptTemplate


def get_entity_extraction_prompt() -> ChatPromptTemplate:
    system_message = (
        "Ты — анализатор текста. "
        "Извлекаешь именованные сущности и отношения между ними.\n"
        "Отвечай ТОЛЬКО валидным JSON. "
        "Никаких приветствий, комментариев, тегов <think>.\n\n"
        "{format_instructions}"
    )

    human_message = (
        "=== ОНТОЛОГИЯ (ТИПЫ СУЩНОСТЕЙ) ===\n"
        "{tbox_classes}\n\n"
        "=== ДОПУСТИМЫЕ ОТНОШЕНИЯ ===\n"
        "{tbox_relations}\n\n"
        "=== УЖЕ ИЗВЕСТНЫЕ СУЩНОСТИ ===\n"
        "{known_entities}\n\n"
        "=== ПРАВИЛА ===\n\n"
        "СУЩНОСТИ:\n"
        "1. ВАЖНО (ОЧИСТКА OCR): Текст часто содержит ошибки распознавания и опечатки (например, 'Блнк' или 'Еанк' вместо 'Банк', бессвязные наборы букв). Исправляй их перед извлечением! Извлекай только нормализованные, осмысленные и грамматически верные названия сущностей.\n"
        "2. ЗАПРЕЩЕНО извлекать в качестве сущностей: заголовки, номера статей, названия глав, разделы, подписи и названия документов (например, 'ГЛАВА 14', 'Статья 49', 'СОДЕРЖАНИЕ', 'Устав.pdf').\n"
        "3. Извлекай МАКСИМАЛЬНО ВОЗМОЖНОЕ количество реальных объектов текста: организации (Банк, Правление, Центральный банк), людей, должности (Председатель), понятия (Коммерческая тайна), процессы (Реорганизация, Финансирование).\n"
        "4. Если сущность из текста совпадает с уже известной, используй ТОЧНО ТО ЖЕ имя и тип.\n"
        "5. Если один объект упоминается разными словами, используй ОДНО каноническое имя.\n"
        "6. Используй тип из онтологии, предпочитая подкласс. Если ни один не подходит — предложи новый (CamelCase, английский).\n\n"
        "ТРОЙКИ (ОТНОШЕНИЯ):\n"
        "7. Извлекай как можно БОЛЬШЕ значимых отношений (троек) между найденными сущностями. Строй плотный граф, который детально описывает логику текста.\n"
        "8. Используй предикаты из списка допустимых.\n"
        "9. subject и object ДОЛЖНЫ точно совпадать с name одной из извлечённых сущностей.\n"
        "10. Без дубликатов. Без самоссылок.\n\n"
        "=== ТЕКСТ ===\n"
        "{text}"
    )

    return ChatPromptTemplate.from_messages(
        [
            ("system", system_message),
            ("human", human_message),
        ],
    )
