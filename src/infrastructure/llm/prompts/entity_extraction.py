"""
Промпт для извлечения сущностей и троек с жёсткими ограничениями.
"""

from langchain_core.prompts import ChatPromptTemplate


def get_entity_extraction_prompt() -> ChatPromptTemplate:
    system_message = (
        "Ты — анализатор текста. "
        "Извлекаешь именованные сущности и отношения между ними.\n"
        "Отвечай ТОЛЬКО валидным JSON. "
        "Никаких приветствий, комментариев, тегов <think>.\n\n"
        "{format_instructions}"
    )

    human_message = (
        "=== ОНТОЛОГИЯ (ТИПЫ СУЩНОСТЕЙ) ===\n"
        "{tbox_classes}\n\n"
        "=== ДОПУСТИМЫЕ ОТНОШЕНИЯ ===\n"
        "{tbox_relations}\n\n"
        "=== ПРАВИЛА ИЗВЛЕЧЕНИЯ ===\n\n"
        "СУЩНОСТИ:\n"
        "1. Извлекай только ЗНАЧИМЫЕ именованные сущности "
        "   (люди, организации, места, ключевые объекты, концепции).\n"
        "2. НЕ извлекай части помещений (пол, стена, окно), "
        "   общие существительные (вода, воздух) и местоимения.\n"
        "3. Если одна сущность упоминается разными словами "
        "   (например «заяц» и «косой»), используй ОДНО каноническое имя.\n"
        "4. Используй тип из онтологии. "
        "   Если есть подходящий подкласс — используй его.\n"
        "5. Новый тип предлагай ТОЛЬКО если ни один существующий "
        "   не подходит (CamelCase, английский).\n\n"
        "ТРОЙКИ (отношения):\n"
        "6. Извлекай ТОЛЬКО семантически важные отношения.\n"
        "7. ОБЯЗАТЕЛЬНО используй предикаты из списка допустимых.\n"
        "8. Новый предикат предлагай ТОЛЬКО если ни один существующий "
        "   не подходит. Формат: UPPER_SNAKE_CASE.\n"
        "9. НЕ создавай цепочки перемещений (MOVED_TO, MOVED_FROM). "
        "   Вместо 10 шагов пути используй одно отношение "
        "   LOCATED_IN к конечному пункту.\n"
        "10. subject и object в тройках ДОЛЖНЫ совпадать "
        "    с полем name одной из извлечённых сущностей (точно).\n"
        "11. НЕ создавай самоссылки (subject = object).\n"
        "12. Максимум 5-7 наиболее важных троек на текст.\n\n"
        "=== ТЕКСТ ===\n"
        "{text}"
    )

    return ChatPromptTemplate.from_messages([
        ("system", system_message),
        ("human", human_message),
    ])