from langchain_core.prompts import ChatPromptTemplate


def get_entity_extraction_prompt() -> ChatPromptTemplate:
    system_message = (
        "Ты — строгий алгоритм извлечения фактов (Information Extraction) для графа знаний.\n"
        "Твоя цель: извлекать только КОНКРЕТНЫЕ объекты реального мира и их взаимодействия.\n"
        "Отвечай ТОЛЬКО валидным JSON. Никаких рассуждений и тегов <think>.\n\n"
        "{format_instructions}"
    )

    human_message = (
        "=== РАЗРЕШЕННЫЕ КЛАССЫ СУЩНОСТЕЙ (T-BOX) ===\n"
        "{tbox_classes}\n\n"
        "=== РАЗРЕШЕННЫЕ ОТНОШЕНИЯ ===\n"
        "{tbox_relations}\n\n"
        "=== УЖЕ ИЗВЕСТНЫЕ СУЩНОСТИ ===\n"
        "{known_entities}\n\n"
        "=== СТРОГИЕ ПРАВИЛА ===\n"
        "1. ТОЛЬКО ИМЕНОВАННЫЕ СУЩНОСТИ: Извлекай людей, компании, названия продуктов, локации, законы, специфические алгоритмы (напр. 'Алгоритм Дейкстры', 'GPT-4').\n"
        "2. БЕЗ АБСТРАКЦИЙ: Категорически ЗАПРЕЩЕНО извлекать общие слова и концепты ('Машинное обучение', 'Рынок', 'Технологии', 'Проблема', 'Анализ данных').\n"
        "3. БЕЗ ДАТ КАК СУЩНОСТЕЙ: Не извлекай годы, века и даты как узлы.\n"
        "4. СТРОГИЙ СЛОВАРЬ: Назначай сущностям ТОЛЬКО классы из списка 'РАЗРЕШЕННЫЕ КЛАССЫ'. Если сущность не подходит ни под один — игнорируй её.\n"
        "5. СТРОГИЕ СВЯЗИ: Используй ТОЛЬКО предикаты из списка 'РАЗРЕШЕННЫЕ ОТНОШЕНИЯ'. Не придумывай свои.\n"
        "6. НОРМАЛИЗАЦИЯ ИМЕН: Имена должны быть в именительном падеже. Запрещено использовать слипшиеся слова (CamelCase), если это не официальное название (как 'FaceID').\n\n"
        "=== ТЕКСТ ===\n"
        "{text}"
    )

    return ChatPromptTemplate.from_messages(
        [
            ("system", system_message),
            ("human", human_message),
        ]
    )
