from langchain_core.prompts import ChatPromptTemplate


def get_entity_extraction_prompt() -> ChatPromptTemplate:
    system_message = (
        "Ты — аналитик текста и эксперт по графам знаний. "
        "Твоя цель — извлекать именованные сущности и отношения между ними.\n"
        "Отвечай ТОЛЬКО валидным JSON. "
        "Никаких приветствий и комментариев.\n\n"
        "{format_instructions}"
    )

    human_message = (
        "=== ОНТОЛОГИЯ (ТИПЫ СУЩНОСТЕЙ) ===\n"
        "{tbox_classes}\n\n"
        "=== ДОПУСТИМЫЕ ОТНОШЕНИЯ ===\n"
        "{tbox_relations}\n\n"
        "=== УЖЕ ИЗВЕСТНЫЕ СУЩНОСТИ ===\n"
        "{known_entities}\n\n"
        "=== ПРАВИЛА ИЗВЛЕЧЕНИЯ СУЩНОСТЕЙ ===\n"
        "1. ЯЗЫК (КРИТИЧНО): Всегда переводи названия сущностей на РУССКИЙ язык (если это не имя собственное бренда или ИТ-продукта). "
        "Никогда не смешивай языки. Например, вместо 'CyberSecurityDepartment' пиши 'Департамент кибербезопасности'.\n"
        "2. РАЗМЕР И ФОРМАТ: Сущность должна быть коротким термином (1-4 слова). ЗАПРЕЩЕНО извлекать целые предложения.\n"
        "3. ТИПИЗАЦИЯ (КРИТИЧНО): Каждая сущность ОБЯЗАНА иметь тип строго из раздела === ОНТОЛОГИЯ ===. Не оставляй тип пустым. "
        "Если точный статус неизвестен (например, 'Акционер' или 'Клиент' может быть и человеком, и компанией), используй базовый класс (Person или Organization).\n"
        "4. СПЕЦИФИЧНОСТЬ: Выбирай максимально точный класс из доступных в онтологии. Например, для 'Конфликта интересов' используй ConflictOfInterest, а не просто Risk.\n"
        "5. ОСМЫСЛЕННОСТЬ: Избегай создания абстрактных сущностей с английскими именами (не 'BankAccount', а 'Банковский счет' или 'Счет клиента').\n"
        "6. ОЧИСТКА OCR: Исправляй опечатки распознавания (например, 'Блнк' -> 'Банк'). ЗАПРЕЩЕНО извлекать заголовки ('ГЛАВА 14').\n"
        "7. ДЕДУПЛИКАЦИЯ: Если сущность из текста совпадает с уже известной, используй ТОЧНО ТО ЖЕ имя и тип из списка === УЖЕ ИЗВЕСТНЫЕ СУЩНОСТИ ===.\n\n"
        "=== ПРАВИЛА ДЛЯ ОТНОШЕНИЙ ===\n"
        "8. DOMAIN И RANGE (КРИТИЧНО): Предикат можно использовать ТОЛЬКО если тип 'subject' входит в Domain предиката, а тип 'object' входит в Range. "
        "Например, если предикат MITIGATES требует (Domain: Process|System -> Range: Risk), то запрещено делать subject сущностью типа Bank!\n"
        "9. РОЛИ И ЛИЦА: Различай конкретных людей (Person) и их должности (Role). Если речь о должности, связывай через HAS_ROLE.\n"
        "10. 'subject' и 'object' ДОЛЖНЫ точно совпадать с 'name' извлечённых сущностей. Без самоссылок и дубликатов.\n\n"
        "Извлеки из чанка минимум 4 триплета"
        "=== ТЕКСТ ===\n"
        "{text}"
    )

    return ChatPromptTemplate.from_messages(
        [
            ("system", system_message),
            ("human", human_message),
        ],
    )
