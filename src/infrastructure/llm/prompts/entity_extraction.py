"""
Промпт для извлечения сущностей и троек — с контекстом известных сущностей.
"""

from langchain_core.prompts import ChatPromptTemplate


def get_entity_extraction_prompt() -> ChatPromptTemplate:
    system_message = (
        "Ты — анализатор текста. "
        "Извлекаешь именованные сущности и отношения между ними.\n"
        "Отвечай ТОЛЬКО валидным JSON. "
        "Никаких приветствий, комментариев, тегов <think>.\n\n"
        "{format_instructions}"
    )

    human_message = (
        "=== ОНТОЛОГИЯ (ТИПЫ СУЩНОСТЕЙ) ===\n"
        "{tbox_classes}\n\n"
        "=== ДОПУСТИМЫЕ ОТНОШЕНИЯ ===\n"
        "{tbox_relations}\n\n"
        "=== УЖЕ ИЗВЕСТНЫЕ СУЩНОСТИ ===\n"
        "{known_entities}\n\n"
        "=== ПРАВИЛА ===\n\n"
        "СУЩНОСТИ:\n"
        "1. Извлекай все именованные сущности из текста: "
        "   персонажей, животных, объекты, места, организации, "
        "   понятия, события.\n"
        "2. Если сущность из текста совпадает с уже известной "
        "   (раздел «УЖЕ ИЗВЕСТНЫЕ СУЩНОСТИ»), используй "
        "   ТОЧНО ТО ЖЕ имя и тип. Не меняй тип!\n"
        "3. Если один персонаж/объект упоминается разными словами "
        "   в тексте, используй ОДНО каноническое имя.\n"
        "4. Используй тип из онтологии, предпочитая подкласс.\n"
        "5. Если ни один тип не подходит — предложи новый "
        "   (CamelCase, английский).\n\n"
        "ТРОЙКИ:\n"
        "6. Извлекай отношения между найденными сущностями.\n"
        "7. Предпочитай предикаты из списка допустимых. "
        "   Если подходящего нет — предложи свой (UPPER_SNAKE_CASE).\n"
        "8. subject и object ДОЛЖНЫ точно совпадать с name "
        "   одной из извлечённых сущностей.\n"
        "9. Без дубликатов. Без самоссылок.\n"
        "10. Если объект перемещается через много мест, "
        "    укажи начальную и конечную точку.\n\n"
        "НЕ извлекай структурные элементы текста (Введение, Заключение, Глава, Рисунок)."
        " НЕ извлекай обобщенные фразы (Современные подходы, Техническая сторона, Проблема)."
        "=== ТЕКСТ ===\n"
        "{text}"
    )

    return ChatPromptTemplate.from_messages(
        [
            ("system", system_message),
            ("human", human_message),
        ]
    )
