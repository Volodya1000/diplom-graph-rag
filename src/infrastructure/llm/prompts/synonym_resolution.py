"""Промпт для поиска синонимов среди сущностей документа."""

from langchain_core.prompts import ChatPromptTemplate

_SYSTEM = (
    "Ты — лингвистический анализатор. "
    "Твоя задача — найти сущности, которые являются СИНОНИМАМИ "
    "(обозначают одно и то же лицо, предмет или понятие, "
    "но названы по-разному).\n\n"
    "ПРАВИЛА:\n"
    "1. Группируй ТОЛЬКО очевидные синонимы.\n"
    "2. Для каждой группы выбери КАНОНИЧЕСКОЕ имя "
    "   (самое частое или информативное).\n"
    "3. НЕ группируй разные сущности! "
    "   «Лиса» и «Волк» — разные персонажи.\n"
    "4. Учитывай тип — синонимы обычно одного типа.\n"
    "5. Учитывай контекст документа — кто кем является.\n\n"
    "ТИПИЧНЫЕ СИНОНИМЫ:\n"
    "  • Старик / Дед / Дедушка — одно лицо\n"
    "  • Старуха / Бабка / Бабушка — одно лицо\n"
    "  • Российская Федерация / Россия / РФ\n"
    "  • СберБанк / Сбер / Сбербанк\n"
    "  • Александр Пушкин / А.С. Пушкин / Пушкин\n\n"
    "Отвечай ТОЛЬКО валидным JSON. "
    "Без комментариев, тегов <think>.\n\n"
    "{format_instructions}"
)

_HUMAN = (
    "=== КОНТЕКСТ ДОКУМЕНТА ===\n"
    "{document_context}\n\n"
    "=== ФРАГМЕНТЫ ТЕКСТА ===\n"
    "{text_snippets}\n\n"
    "=== СУЩНОСТИ ===\n"
    "{entities_list}\n\n"
    "Найди группы синонимов среди сущностей выше. "
    "Если синонимов нет — верни {{\"groups\": []}}."
)


def get_synonym_resolution_prompt() -> ChatPromptTemplate:
    return ChatPromptTemplate.from_messages([
        ("system", _SYSTEM),
        ("human", _HUMAN),
    ])